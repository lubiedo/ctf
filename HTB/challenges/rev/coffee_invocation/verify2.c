uint8_t* complex_sort(uint8_t* s, uint8_t* o, size_t len, bool sort, int charset)
{
	for (int i = 0; i<len; i++)
	{
		o[i] = charsets[s[i]];
	}
	if (sort)
	{
		qsort((int **)o, len, sizeof(uint8_t), compare);
	}
	//dump(o, len);
	return o;
}

int _index(const uint8_t* s, uint8_t h, size_t len)
{
	int pos = 0;
	for (;pos < len; pos++)
		if (s[pos] == h)
			break;
	return pos;
}

